package com.example.afinal;

public class User {
    public String nama, email, nomor, alamat, type;

    public User(){

    }
    public User(String nama, String email, String nomor, String alamat, String type){
        this.nama = nama;
        this.alamat = alamat;
        this.nomor = nomor;
        this.email = email;
        this.type = type;


    }

}
